    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user =  Users::find($id);
        if ($user->student_id) {
            $student = Students::find($user->student_id);
            $student->email = null;
            $student->save();
        } else if ($user->lecturer_id) {
            $lecturer = Lecturers::find($user->lecturer_id);
            $lecturer->email = null;
            $lecturer->save();
        }
        $user->delete();
        return redirect()->route('admin.dashboard')->with('success', 'Xóa thành công');
    }
